	public String getIdentify() {
		return identify;
	}
	public void setIdentify(String identify) {
		this.identify = identify;
	}
	public String getBlnoMin() {
		return blnoMin;
	}
	public void setBlnoMin(String blnoMin) {
		this.blnoMin = blnoMin;
	}
	public String getBlnoMax() {
		return blnoMax;
	}
	public void setBlnoMax(String blnoMax) {
		this.blnoMax = blnoMax;
	}
